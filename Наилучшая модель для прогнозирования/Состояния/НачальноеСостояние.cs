using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Наилучшая_модель_для_прогнозирования
{
    public class НачальноеСостояние : БазовоеСостояние, IСостояние
    {
        private readonly АвтоматПрогнозирования автомат;

        public НачальноеСостояние(АвтоматПрогнозирования автоматПрогнозирования)
        {
            this.автомат = автоматПрогнозирования;
        }

        public void ПерейтиКСледующему()
        {
            автомат.ОбновитьСтатусСостояния("Начальное");

            if (Check(автомат.Ra2, автомат.Rb2, автомат.Rc2))
            {
                автомат.УстановитьСостояние(автомат.ABСостояние);
            }

            if (Check(автомат.Ra2, автомат.Rc2, автомат.Rb2))
            {
                автомат.УстановитьСостояние(автомат.ACСостояние);
            }

            if (Check(автомат.Rb2, автомат.Ra2, автомат.Rc2))
            {
                автомат.УстановитьСостояние(автомат.BAСостояние);
            }

            if (Check(автомат.Rb2, автомат.Rc2, автомат.Ra2))
            {
                автомат.УстановитьСостояние(автомат.BCСостояние);
            }

            if (Check(автомат.Rc2, автомат.Ra2, автомат.Rb2))
            {
                автомат.УстановитьСостояние(автомат.CAСостояние);
            }
        }
    }
}
